package kz.dossier.dto;

public class PdlDto {
    private String iin;
    private String fio;
    private String position;
    private String oblast;
    private String organ;
    private String iinSpouse;
    private String fullNameSpouse;
    private String organSpouse;
    private String positionSpouse;

    public String getIin() {
        return iin;
    }

    public void setIin(String iin) {
        this.iin = iin;
    }

    public String getFio() {
        return fio;
    }

    public void setFio(String fio) {
        this.fio = fio;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getOblast() {
        return oblast;
    }

    public void setOblast(String oblast) {
        this.oblast = oblast;
    }

    public String getOrgan() {
        return organ;
    }

    public void setOrgan(String organ) {
        this.organ = organ;
    }

    public String getIinSpouse() {
        return iinSpouse;
    }

    public void setIinSpouse(String iinSpouse) {
        this.iinSpouse = iinSpouse;
    }

    public String getFullNameSpouse() {
        return fullNameSpouse;
    }

    public void setFullNameSpouse(String fullNameSpouse) {
        this.fullNameSpouse = fullNameSpouse;
    }

    public String getOrganSpouse() {
        return organSpouse;
    }

    public void setOrganSpouse(String organSpouse) {
        this.organSpouse = organSpouse;
    }

    public String getPositionSpouse() {
        return positionSpouse;
    }

    public void setPositionSpouse(String positionSpouse) {
        this.positionSpouse = positionSpouse;
    }
}
