package kz.dossier.riskDto;

public class FirstCreditBuroDto {
    private String region;
    private String amount;
    private String sum;
    private String maxDay;
    private String actuality;
    private String nameOfFin;
    private String totalAmount;
    private String totalSum;
    private String maxDay2;

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getSum() {
        return sum;
    }

    public void setSum(String sum) {
        this.sum = sum;
    }

    public String getMaxDay() {
        return maxDay;
    }

    public void setMaxDay(String maxDay) {
        this.maxDay = maxDay;
    }

    public String getActuality() {
        return actuality;
    }

    public void setActuality(String actuality) {
        this.actuality = actuality;
    }

    public String getNameOfFin() {
        return nameOfFin;
    }

    public void setNameOfFin(String nameOfFin) {
        this.nameOfFin = nameOfFin;
    }

    public String getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(String totalAmount) {
        this.totalAmount = totalAmount;
    }

    public String getTotalSum() {
        return totalSum;
    }

    public void setTotalSum(String totalSum) {
        this.totalSum = totalSum;
    }

    public String getMaxDay2() {
        return maxDay2;
    }

    public void setMaxDay2(String maxDay2) {
        this.maxDay2 = maxDay2;
    }
}
