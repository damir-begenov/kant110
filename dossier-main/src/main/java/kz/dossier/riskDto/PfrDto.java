package kz.dossier.riskDto;

public class PfrDto {
    private String pfr;
    private String sum;
    private String measure;
    private String bvu;
    private String info;

    public String getPfr() {
        return pfr;
    }

    public void setPfr(String pfr) {
        this.pfr = pfr;
    }

    public String getSum() {
        return sum;
    }

    public void setSum(String sum) {
        this.sum = sum;
    }

    public String getMeasure() {
        return measure;
    }

    public void setMeasure(String measure) {
        this.measure = measure;
    }

    public String getBvu() {
        return bvu;
    }

    public void setBvu(String bvu) {
        this.bvu = bvu;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
